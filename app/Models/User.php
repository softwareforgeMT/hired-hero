    public function placementProfile()
    {
        return $this->hasOne(PlacementProfile::class);
    }

    public function coverLetters()
    {
        return $this->hasMany(CoverLetter::class);
    }

    public function resumeSubscriptions()
    {
        return $this->hasMany(ResumeSubscription::class);
    }

    /**
     * Get active subscription from UserSubscription table
     */
    public function getActiveSubscription()
    {
        return $this->subscriptions()
                    ->where('status', 'active')
                    ->where(function ($query) {
                        $query->whereNull('expires_at')
                              ->orWhere('expires_at', '>', now());
                    })
                    ->orderBy('id', 'desc')
                    ->first();
    }

    /**
     * Check if user has active subscription
     */
    public function hasActiveSubscription()
    {
        return $this->getActiveSubscription() !== null;
    }

    /**
     * Get promo codes assigned to this user
     */
    public function promoCodes()
    {
        return $this->belongsToMany(PromoCode::class, 'promo_code_user')
                    ->withPivot('used', 'used_at')
                    ->withTimestamps();
    }

    /**
     * Get active (unused) promo codes for this user
     */
    public function getActivePromoCodes()
    {
        return $this->promoCodes()
                    ->where('active', true)
                    ->where(function ($query) {
                        $query->whereNull('expires_at')
                              ->orWhere('expires_at', '>', now());
                    })
                    ->wherePivot('used', false)
                    ->get();
    }

    /**
     * Get a specific promo code validity for this user
     */
    public function canUsePromoCode($code): bool
    {
        $promoCode = PromoCode::where('code', strtoupper($code))->first();

        if (!$promoCode) {
            return false;
        }

        return $promoCode->canUserUse($this);
    }
}
